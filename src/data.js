export function avg(arr) {
  if (!arr.length) return 0;
  return arr.reduce((a, b) => a + b, 0) / arr.length;
}

export function getRanking() {
  return COLABORADORES.map((c) => {
    const tiempos = rawData
      .filter((d) => d.colaborador === c && d.estacion === "PLANCHA")
      .map((d) => d.tiempo);
    return { colaborador: c, promedio: tiempos.length ? +avg(tiempos).toFixed(2) : null };
  })
    .filter((d) => d.promedio !== null)
    .sort((a, b) => a.promedio - b.promedio);
}

export function getByEstacion() {
  return COLABORADORES.flatMap((c) =>
    ESTACIONES.map((e) => {
      const tiempos = rawData
        .filter((d) => d.colaborador === c && d.estacion === e)
        .map((d) => d.tiempo);
      return tiempos.length
        ? { colaborador: c, estacion: e, promedio: +avg(tiempos).toFixed(2) }
        : null;
    }).filter(Boolean)
  );
}

export function getByHora() {
  const map = {};
  rawData.forEach((d) => {
    const h = d.horaInicio.split(":")[0] + ":00";
    if (!map[h]) map[h] = [];
    map[h].push(d.tiempo);
  });
  return Object.entries(map)
    .map(([hora, tiempos]) => ({ hora: hora + " h", promedio: +avg(tiempos).toFixed(2) }))
    .sort((a, b) => a.hora.localeCompare(b.hora));
}

export function getHistorial(colaborador, estacion) {
  return rawData
    .filter((d) => d.colaborador === colaborador && d.estacion === estacion)
    .sort((a, b) => (a.fecha + a.horaInicio).localeCompare(b.fecha + b.horaInicio));
}
